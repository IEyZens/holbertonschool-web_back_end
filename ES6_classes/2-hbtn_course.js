class HolbertonCourse {
  constructor(name, length, students) {
    this._name = name;
    this._length = length;
    this._students = students;
  }

  get name() {
    return this.name;
  }

  set name(name) {
    name;
  }

  get length() {
    return this.length;
  }

  set length(length) {
    length;
  }

  get students() {
    return this.students;
  }

  set students(students) {
    students;
  }
}

HolbertonCourse();
